// ── xchg bx,bx marker callback ───────────────────────────────────────────
// Called when Pin detects xchg bx,bx in the application.
// EAX holds the marker id (1-5).
VOID MarkerHit(UINT32 marker_id)
{
    if (marker_id == 0 || marker_id >= 6) return;
    if (phase_seen[marker_id]) return; // only record first occurrence

    phase_seen[marker_id]  = true;
    phase_instr[marker_id] = instrCount + 1;

    if(phase_seen[INFERENCE_FIRST_PASS_START])
        knob_skip_instr_count = instrCount;

    const char* names[] = {"", "PREPROCESSING_START", "PREPROCESSING_END",
                           "INFERENCE_PASS1_START", "INFERENCE_PASS1_END",
                           "INFERENCE_PASS2_END"};
    std::cout << ">>> MARKER " << marker_id << " [" << names[marker_id] << "]"
              << " at instrCount = " << instrCount + 1 << std::endl;    

    if (marker_id == PREPROCESSING_END && phase_seen[PREPROCESSING_START])
    {   
        std::cout << "    Preprocessing instructions: "<< ( phase_instr[PREPROCESSING_END] - phase_instr[PREPROCESSING_START]) << std::endl;    
    }

    if (marker_id == INFERENCE_FIRST_PASS_END && phase_seen[INFERENCE_FIRST_PASS_START])
    {
        UINT64 pass_len = phase_instr[INFERENCE_FIRST_PASS_END] - phase_instr[INFERENCE_FIRST_PASS_START];
        std::cout << "    One inference pass instructions: " << pass_len << std::endl;
    }

    // can be removed. As of now wont execute as we are ending after first pass end
    if (marker_id == INFERENCE_SECOND_PASS_END && phase_seen[INFERENCE_FIRST_PASS_START])
    {
        UINT64 two_passes = phase_instr[INFERENCE_SECOND_PASS_END] - phase_instr[INFERENCE_FIRST_PASS_START];
        std::cout << "    Two inference passes instructions: " << two_passes << std::endl;
    }
}

