// Is called for every instruction and instruments reads and writes
VOID Instruction(INS ins, VOID *v)
{
    uint32_t memOperands = INS_MemoryOperandCount(ins);
    // std::cout << std::endl;
    // std::cout << "memOperandscount = " << memOperands << std::endl ;

    // ── Detect xchg bx,bx marker ─────────────────────────────────────────
    // xchg bx,bx: opcode 0x87 DB with both operands being BX/RBX
    if (INS_Opcode(ins) == XED_ICLASS_XCHG &&
        INS_OperandCount(ins) >= 2 &&
        INS_OperandIsReg(ins, 0) && INS_OperandIsReg(ins, 1) &&
        (INS_OperandReg(ins, 0) == REG_BX || INS_OperandReg(ins, 0) == REG_RBX) &&
        (INS_OperandReg(ins, 1) == REG_BX || INS_OperandReg(ins, 1) == REG_RBX))
    {
        // Read marker id from EAX register value at runtime
        INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)MarkerHit,
                       IARG_REG_VALUE, REG_EAX,
                       IARG_CALL_ORDER, CALL_ORDER_FIRST,
                       IARG_END);
    }

    // // ── Track function names during skip phase ────────────────────────────
    // RTN rtn = INS_Rtn(ins);
    // if (RTN_Valid(rtn) && INS_Address(ins) == RTN_Address(rtn))
    // {
    //     std::string* name = new std::string(RTN_Name(rtn));
    //     INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)FuncEntry,
    //                    IARG_PTR, name->c_str(),
    //                    IARG_END);
    // }

    // begin each instruction with this function
    uint32_t opcode = INS_Opcode(ins);
    std::string opcodeStr = INS_Mnemonic(ins);
    // std::cout << "Opcode = " << opcode << " - " << opcodeStr << std::endl;
    INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)BeginInstruction,
                   IARG_INST_PTR,
                   IARG_UINT32, opcode,
                   IARG_UINT32, memOperands,
                   IARG_UINT32, INS_Size(ins), 
                   IARG_CALL_ORDER, CALL_ORDER_FIRST+1,
                   IARG_END);

    // instrument branch instructions
    if (INS_IsBranch(ins))
        INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)BranchOrNot,
                       IARG_BRANCH_TAKEN,
                       IARG_END);

    // instrument register reads
    uint32_t readRegCount = INS_MaxNumRRegs(ins);
    // std::cout << "readRegCount = " << readRegCount << std::endl;

    for (uint32_t i = 0; i < readRegCount; i++)
    {
        uint32_t regNum = INS_RegR(ins, i);

        INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)RegRead,
                       IARG_UINT32, regNum,
                       IARG_UINT32, i,
                       IARG_END);
    }

    // instrument register writes
    uint32_t writeRegCount = INS_MaxNumWRegs(ins);
    // std::cout << "writeRegCount = " << readRegCount << std::endl;

    for (uint32_t i = 0; i < writeRegCount; i++)
    {
        uint32_t regNum = INS_RegW(ins, i);

        INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)RegWrite,
                       IARG_UINT32, regNum,
                       IARG_UINT32, i,
                       IARG_END);
    }

    // instrument memory reads and writes

    // Iterate over each memory operand of the instruction.
    for (uint32_t memOp = 0; memOp < memOperands; memOp++)
    {
        if (INS_MemoryOperandIsRead(ins, memOp))
        {
            uint32_t read_size = INS_MemoryOperandSize(ins, memOp);
            if (read_size > MAX_MEMORY_SIZE)
            {
                // std::cout << "MemoryOperand is Read - " << memOp << ", size = " << read_size << std::endl;
                skip_curr_instr = true;
                skipInstructionCount++;
                readSkipInstrCount++;
                break;
            }

            INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)MemoryRead,
                           IARG_MEMORYOP_EA, memOp,
                           IARG_UINT32, memOp,
                           IARG_UINT32, read_size,
                           IARG_END);
        }

        if (INS_MemoryOperandIsWritten(ins, memOp))
        {
            uint32_t write_size = INS_MemoryOperandSize(ins, memOp);
            if (write_size > MAX_MEMORY_SIZE)
            {
                // std::cout << "MemoryOperand is Write - " << memOp << ", size = " << write_size << std::endl;
                skip_curr_instr = true;
                skipInstructionCount++;
                writeSkipInstrCount++;
                break;
            }
            INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)MemoryWriteCaptureAddress,
                           IARG_MEMORYOP_EA, memOp,
                           IARG_UINT32, memOp,
                           IARG_END);

            if (INS_HasFallThrough(ins))
            {
                INS_InsertCall(ins, IPOINT_AFTER, (AFUNPTR)MemoryWriteCaptureValue,
                               IARG_UINT32, memOp,
                               IARG_UINT32, write_size,
                               IARG_END);
            }
            else if (INS_IsCall(ins))
            {
                // std::cout << "MemoryOperand is Write but ISCall - " << memOp << std::endl;
                INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)MemoryWriteValueForCall, 
                               IARG_UINT32, INS_Size(ins),
                               IARG_UINT32, memOp,
                               IARG_END);
            }
            else
            {
                // if (debug)
                // {
                    std::cout << "Warning: Memory write instrumentation at IPOINT_AFTER for instruction ** " << INS_Disassemble(ins) << " ** is invalid" << std::endl;
                    std::cout << "Opcode: " << std::dec << INS_Opcode(ins) << std::endl;
                // }
            }
        }
    }

    // finalize each instruction with this function
    if (INS_HasFallThrough((ins)))
        INS_InsertCall(ins, IPOINT_AFTER, (AFUNPTR)EndInstruction,
                       IARG_CALL_ORDER, CALL_ORDER_LAST,
                       IARG_END);
    else
        INS_InsertCall(ins, IPOINT_BEFORE, (AFUNPTR)EndInstruction,
                       IARG_CALL_ORDER, CALL_ORDER_LAST,
                       IARG_END);
}

/*!
 * Print out analysis results.
 * This function is called when the application exits.
 * @param[in]   code            exit code of the application
 * @param[in]   v               value specified by the tool in the
 *                              PIN_AddFiniFunction function call
 */
VOID Fini(INT32 code, VOID *v)
{
    // close the file if it hasn't already been closed
    if (!output_file_closed)
    {
        fclose(out);
        output_file_closed = true;
    }
       std::cout << "\n========== TRACE GENERATION SUMMARY ==========" << std::endl;
    std::cout << "Total instructions executed:     " << instrCount << std::endl;
    std::cout << "Skip instructions :          " << knob_skip_instr_count << std::endl;
    std::cout << "Trace instructions:         " << knob_trace_instr_count << std::endl;

    std::cout << "\n--- Phase Boundaries (instrCount values) ---" << std::endl;
    const char* pnames[] = {"", "PREPROCESSING_START", "PREPROCESSING_END",
                            "INFERENCE_PASS1_START", "INFERENCE_PASS1_END",
                            "INFERENCE_PASS2_END"};
    for (int i = 1; i <= 5; i++)
    {    std::cout << "  Marker " << i << " [" << pnames[i] << "]: ";
        if (phase_seen[i])
            std::cout << phase_instr[i];
        else
            std::cout << "NOT SEEN";
        std::cout << std::endl;
    }

    if (phase_seen[PREPROCESSING_START] && phase_seen[PREPROCESSING_END])
        std::cout << "\nPreprocessing instructions:      "
                  << (phase_instr[PREPROCESSING_END] - phase_instr[PREPROCESSING_START]) << std::endl;

    if (phase_seen[INFERENCE_FIRST_PASS_START] && phase_seen[INFERENCE_FIRST_PASS_END])
    {
        UINT64 pass_len = phase_instr[INFERENCE_FIRST_PASS_END] - phase_instr[INFERENCE_FIRST_PASS_START];
        std::cout << "One inference pass instructions: " << pass_len << std::endl;
        std::cout << "\n--- Recommended ChampSim Settings ---" << std::endl;
        std::cout << "  ChampSim warmup:    " << 0.2 * pass_len
                  << "  (~20% of one pass)" << std::endl;
        std::cout << "  ChampSim simulate:  " << 0.8 * pass_len
                  << "  (remaining 80% of one pass)" << std::endl;
    }

    std::cout << "\n--- Memory Access Stats ---" << std::endl;
    std::cout << "Skip Instruction count = " << skipInstructionCount << std::endl;
    std::cout << "Skip Read Instruction count = " << readSkipInstrCount << std::endl;
    std::cout << "Skip Write Instruction count = " << writeSkipInstrCount << std::endl;
    std::cout << "Unaligned access count = " << unalignedAccessCount << std::endl;
    std::cout << "==============================================" << std::endl;
}

/*!
 * The main procedure of the tool.
 * This function is called when the application image is loaded but not yet started.
 * @param[in]   argc            total number of elements in the argv array
 * @param[in]   argv            array of command line arguments,
 *                              including pin -t <toolname> -- ...
 */
int main(int argc, char *argv[])
{
    // Initialize PIN library. Print help message if -h(elp) is specified
    // in the command line or the command line is invalid
    if (PIN_Init(argc, argv))
        return Usage();

    if (KnobEnableTraceWriting.Value())
    {
        const char *fileName = KnobOutputFile.Value().c_str();

        out = fopen(fileName, "wb");
        if (!out)
        {
            std::cout << "Couldn't open output trace file. Exiting." << std::endl;
            exit(1);
        }
    }
    else
    {
        out = nullptr;
    }    

    if(!KnobSetMarkerMode.Value())
        knob_skip_instr_count = KnobSkipInstructions.Value();

    // Register function to be called to instrument instructions
    INS_AddInstrumentFunction(Instruction, 0);

    // Register function to be called when the application exits
    PIN_AddFiniFunction(Fini, 0);

    // cerr <<  "===============================================" << endl;
    // cerr <<  "This application is instrumented by the Champsim Trace Generator" << endl;
    // cerr <<  "Trace saved in " << KnobOutputFile.Value() << endl;
    // cerr <<  "===============================================" << endl;

    // Start the program, never returns
    PIN_StartProgram();

    return 0;
}

